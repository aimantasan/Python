name = " Aiman Fakhrullah Mantasan "

print(name) # Print String
print (len(name)) #len = how long the length of the string
print(name.find("a")) # .find =  Find which index the letter is
print(name.capitalize()) # .capitalize = Only capitalize the first letter on a word string
print(name.upper()) # .upper = Turn all letters to UpperCase
print(name.lower()) # .lower = Turn all letters to LowerCase
print(name.isdigit()) # .isdigit = Check if string contains digit (False if no number)
print(name.isalpha()) # .isalpha = Check if string is alphabet (False if there's space)
print(name.count("a")) # .count =  Count how many alphabet are there in the string
print(name.replace ("a", "o")) # .replace = replace old character to a new one
print(name*3) #Call the same string multiple times