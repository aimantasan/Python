import math #import math module

pi = -3.14

x,y,z = 1,2,3

print(round(pi))        # Round to nearest whole integer
print(math.ceil(pi))    # Round up to nearest whole number
print(math.floor(pi))   # Round down to nearest whole number
print(abs(pi))          # Give the absolute value of a number, how far away from 0
print(pow(pi,2))        # Raise a number to the power function Requires:(Base, Power Exponant)
print(math.sqrt(420))   # Give a square root value of a number
print(max(x,y,z))       # Find the maximum value from variables
print(min(x,y,z))       # Find the minimum value from variables